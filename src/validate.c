#include "fillit.h"

#define BIT_AT(SET, X, Y, N) (((SET) >> (((X) * (N)) + (Y))) & 1)

t_u08	fillit_validate(t_elm elm)
{
	t_i08 x;
	t_i08 y;
	t_u08 c;

	c = 0;
	x = -1;
	while (++x < 4 && (y = -1) < 0)
		while (++y < 4)
			if (BIT_AT(elm, x, y, 4))
			{
				if (x < 3 && BIT_AT(elm, x + 1, y, 4))
					++c;
				if (y < 3 && BIT_AT(elm, x, y + 1, 4))
					++c;
			}
	return ((t_u08)(c >= 3));
}
